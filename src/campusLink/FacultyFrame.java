        try (Connection con = DriverManager.getConnection(url, usrn, pass);
            Statement st = con.createStatement()) {

            // Check if the column exists in the table
            DatabaseMetaData metaData = con.getMetaData();
            ResultSet rs = metaData.getColumns(null, null, "attendance", date);
            if (rs.next()) {
                found = true;
            }

            if (found) {
                // Column found, retrieve the data
                String selectQuery = "SELECT FIRST_NAME, LAST_NAME, REGISTRATION_NUMBER, BRANCH, "+date+" FROM attendance";
                PreparedStatement selectStatement = con.prepareStatement(selectQuery);
                rs = selectStatement.executeQuery();
                attenTable.setModel(DbUtils.resultSetToTableModel(rs));
            } else {
                // Column not found, ask user if they want to create a new column
                int confirmed = JOptionPane.showConfirmDialog(null, "No record found for the date '" + date + "'. Do you want to create a new column?", "Confirmation", JOptionPane.YES_NO_OPTION);

                if (confirmed == JOptionPane.YES_OPTION) {
                    // Create the new column
                    String addColumnQuery = "ALTER TABLE attendance ADD COLUMN " + date + " VARCHAR(50) DEFAULT 'absent'";
                    st.executeUpdate(addColumnQuery);

                    // Update the existing rows with the default value
                    String updateRowsQuery = "UPDATE attendance SET " + date + " = 'absent'";
                    st.executeUpdate(updateRowsQuery);

                    // Display the updated table
                    String selectQuery = "SELECT FIRST_NAME, LAST_NAME, REGISTRATION_NUMBER, BRANCH, " + date + " FROM attendance";
                    PreparedStatement selectStatement = con.prepareStatement(selectQuery);
                    rs = selectStatement.executeQuery();
                    attenTable.setModel(DbUtils.resultSetToTableModel(rs));
                } else {
                    System.out.println("No record found!");
                    JOptionPane.showMessageDialog(null, "No record found", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (SQLException ex) {
            System.out.println(ex);
        }

        // Create a custom cell renderer
        DefaultTableCellRenderer cellRenderer = new DefaultTableCellRenderer() {
            @Override
            public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
                // Call the super method to perform default rendering
                super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);

                // Get the value of the specified row and column
                Object targetValue = table.getValueAt(row, 4);

                // Customize the cell color based on the target value
                if (targetValue != null && targetValue.toString().equals("absent")) {
                    setBackground(Color.GRAY); // Set the desired background color
                } else {
                    setBackground(table.getBackground()); // Set the default background color
                }

                return this;
            }
        };

        // Set the cell renderer for the entire row
        attenTable.setDefaultRenderer(Object.class, cellRenderer);

