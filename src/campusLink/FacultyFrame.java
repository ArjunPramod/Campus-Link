    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        DefaultTableModel model = (DefaultTableModel) studentMarksTable.getModel();

        // Iterate over the rows of the table
        int rowCount = model.getRowCount();
        for (int i = 0; i < rowCount; i++) {
            // Get the values from the table for each row
            String registrationNumber = model.getValueAt(i, 2).toString(); // Assuming registration number is in the third column
            int javaMarks = Integer.parseInt(model.getValueAt(i, 4).toString()); // Assuming Java marks is in the fifth column
            int cLanguageMarks = Integer.parseInt(model.getValueAt(i, 5).toString()); // Assuming C Language marks is in the sixth column
            int cppLanguageMarks = Integer.parseInt(model.getValueAt(i, 6).toString()); // Assuming C++ Language marks is in the seventh column
            int pythonMarks = Integer.parseInt(model.getValueAt(i, 7).toString()); // Assuming Python marks is in the eighth column

            // Construct the update query for each language column
            String updateJavaQuery = "UPDATE studentmarks SET JAVA = " + javaMarks + " WHERE REGISTRATION_NUMBER = '" + registrationNumber + "'";
            String updateCLanguageQuery = "UPDATE studentmarks SET C_Language = " + cLanguageMarks + " WHERE REGISTRATION_NUMBER = '" + registrationNumber + "'";
            String updateCPPLanguageQuery = "UPDATE studentmarks SET CPP_Language = " + cppLanguageMarks + " WHERE REGISTRATION_NUMBER = '" + registrationNumber + "'";
            String updatePythonQuery = "UPDATE studentmarks SET Python = " + pythonMarks + " WHERE REGISTRATION_NUMBER = '" + registrationNumber + "'";

            try (Connection con = DriverManager.getConnection(url, usrn, pass);
                 Statement st = con.createStatement()) {
                // Execute the update queries for each language column
                st.executeUpdate(updateJavaQuery);
                st.executeUpdate(updateCLanguageQuery);
                st.executeUpdate(updateCPPLanguageQuery);
                st.executeUpdate(updatePythonQuery);
            } catch (SQLException ex) {
                System.out.println(ex);
            }
        }

        // Display a success message or perform any necessary actions after updating the records
        JOptionPane.showMessageDialog(null, "Records updated successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
    
    }//GEN-LAST:event_jButton4ActionPerformed

